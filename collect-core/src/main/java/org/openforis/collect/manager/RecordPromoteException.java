package org.openforis.collect.manager;

/**
 * @author M. Togna
 *
 */
public class RecordPromoteException extends Exception {

	private static final long serialVersionUID = 1L;

	public RecordPromoteException() {
		super();
	}

	public RecordPromoteException(String message, Throwable cause) {
		super(message, cause);
	}

	public RecordPromoteException(String message) {
		super(message);
	}

	public RecordPromoteException(Throwable cause) {
		super(cause);
	}
	
}
